package org.example;

import java.util.Scanner;

public class OutputVariabel {
    public static void main(String[] args) {
        double nilaiA = 2.0;
        double nilaiB = 4.0;
        double nilaiC = 6.0;

        // Menggunakan printf untuk memformat desimal menjadi 6 digit di belakang koma
        System.out.printf("NILAI A = %.6f%n", nilaiA);
        System.out.printf("NILAI B = %.6f%n", nilaiB);
        System.out.printf("NILAI C = %.6f%n", nilaiC);
    }
}
